class LogBookActivityFilterUtil {
  activityFilter(data) {
    switch (data) {
      case '6':
        return 0;
      case '4':
        return 1;
      case '2':
        return 2;
      case '9':
        return 3;
      case '7':
        return 4;
      case '8':
        return 5;
      case '5':
        return 6;
      case '3':
        return 7;
    }
  }
}
